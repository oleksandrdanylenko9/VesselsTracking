using MediatR;
using Microsoft.EntityFrameworkCore;
using VesselsTracking.CQRS.Queries;
using VesselsTracking.Persistence;

namespace VesselsTracking.API
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            builder.Services.AddDbContext<VesselsDbContext>(opt => opt.UseInMemoryDatabase("Vessels"));
            builder.Services.AddMediatR(typeof(GetTrackedVesselsQuery));

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}